    private void actualizarTable1(){
        
        String [] encabezado = new String[4];
        encabezado[0]="Código";
        encabezado[1]="Votos";
        encabezado[2]="Número Votantes";
        encabezado[3]="Descripción";
        
        
        var datos = new Object[this.eleccionControl.listar().size()][4];
        
        var i=0;
        for(var eleccion:this.eleccionControl.listar())
        {
            datos[i][0]=eleccion.getCodigo();
            datos[i][1]=eleccion.getVotos();
            datos[i][2]=eleccion.getNumeroVotante();
            datos[i][3]=eleccion.getDescripcion();
            i++;
        }
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
        
    }
